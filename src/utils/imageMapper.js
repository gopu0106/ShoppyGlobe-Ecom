export const getEnhancedImage = (id, category) => {
  // First try direct ID mapping
  if (enhancedImages[id]) {
    return enhancedImages[id];
  }
  
  // Fallback to category-based image
  if (category && categoryFallbacks[category.toLowerCase()]) {
    return categoryFallbacks[category.toLowerCase()];
  }
  
  // Ultimate fallback - generic product placeholder
  return null;
};
